#!/bin/bash
# ============================================================ #
# ==      –ò–ù–°–¢–†–£–ú–ï–ù–¢ ¬´–†–ï–®–ê–õ–ê¬ª v2.68 - –†–ï–§–ê–ö–¢–û–†–ò–ù–ì –ú–ï–ù–Æ        == #
# ============================================================ #
#
# –¢–æ—á–∫–∞ –≤—Ö–æ–¥–∞. –≠—Ç–æ—Ç —Å–∫—Ä–∏–ø—Ç ‚Äî –ø—Ä–æ—Ä–∞–±. –û–Ω —Ç–æ–ª—å–∫–æ –æ—Ç–¥–∞—ë—Ç –∫–æ–º–∞–Ω–¥—ã
# –º–æ–¥—É–ª—è–º –∏ –æ—Ç—Ä–∏—Å–æ–≤—ã–≤–∞–µ—Ç –≥–ª–∞–≤–Ω–æ–µ –º–µ–Ω—é.
#
# @menu.manifest
#
# @item( main | d | üóëÔ∏è  –°–Ω–µ—Å—Ç–∏ –†–µ—à–∞–ª—É | _reshala_uninstall_wrapper | 90 | 90 | –£–¥–∞–ª—è–µ—Ç –≤—Å–µ —Ñ–∞–π–ª—ã –∏ –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—é –†–µ—à–∞–ª—ã. )
#

set -uo pipefail

# –ü—Ä–æ–≤–µ—Ä—è–µ–º –º–∏–Ω–∏–º–∞–ª—å–Ω—É—é –≤–µ—Ä—Å–∏—é bash (4.0+) ‚Äî –Ω—É–∂–µ–Ω –¥–ª—è mapfile, –∞—Å—Å–æ—Ü–∏–∞—Ç–∏–≤–Ω—ã—Ö –º–∞—Å—Å–∏–≤–æ–≤ –∏ —Ç.–¥.
if (( BASH_VERSINFO[0] < 4 )); then
    echo "[FATAL] –¢—Ä–µ–±—É–µ—Ç—Å—è bash 4.0 –∏–ª–∏ –Ω–æ–≤–µ–µ. –¢–µ–∫—É—â–∞—è –≤–µ—Ä—Å–∏—è: ${BASH_VERSION}"
    echo "–ù–∞ macOS —É—Å—Ç–∞–Ω–æ–≤–∏ —Å–≤–µ–∂–∏–π bash: brew install bash"
    echo "–ò –∑–∞–ø—É—Å–∫–∞–π —Å–∫—Ä–∏–ø—Ç —á–µ—Ä–µ–∑: /opt/homebrew/bin/bash $(readlink "$0" 2>/dev/null || echo "$0")"
    exit 1
fi

# –û–ø—Ä–µ–¥–µ–ª—è–µ–º –†–ï–ê–õ–¨–ù–û–ï –º–µ—Å—Ç–æ–ø–æ–ª–æ–∂–µ–Ω–∏–µ —Å–∫—Ä–∏–ø—Ç–∞, –¥–∞–∂–µ –µ—Å–ª–∏ –æ–Ω –∑–∞–ø—É—â–µ–Ω —á–µ—Ä–µ–∑ —Å–∏–º–ª–∏–Ω–∫
SOURCE="${BASH_SOURCE[0]}"
while [ -h "$SOURCE" ]; do
  DIR="$( cd -P "$( dirname "$SOURCE" )" && pwd )"
  SOURCE="$(readlink "$SOURCE")"
  [[ $SOURCE != /* ]] && SOURCE="$DIR/$SOURCE"
done
export SCRIPT_DIR="$( cd -P "$( dirname "$SOURCE" )" && pwd )"

readonly VERSION="v2.68"

# ============================================================ #
#              –ü–û–î–ì–û–¢–û–í–ö–ê –ò –ó–ê–ì–†–£–ó–ö–ê –ö–û–ú–ü–û–ù–ï–ù–¢–û–í               #
# ============================================================ #

# –ó–∞–≥—Ä—É–∂–∞–µ–º –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—é
if [ -f "${SCRIPT_DIR}/config/reshala.conf" ]; then
    source "${SCRIPT_DIR}/config/reshala.conf"
else
    echo "[FATAL ERROR] Configuration file config/reshala.conf not found."
    exit 1
fi

# –ó–∞–≥—Ä—É–∂–∞–µ–º –æ–±—â–∏–µ –∏–Ω—Å—Ç—Ä—É–º–µ–Ω—Ç—ã
if [ -f "${SCRIPT_DIR}/modules/core/common.sh" ]; then
    source "${SCRIPT_DIR}/modules/core/common.sh"
else
    echo "[FATAL ERROR] Common tools module modules/core/common.sh not found."
    exit 1
fi

# –ó–∞–≥—Ä—É–∂–∞–µ–º –ù–û–í–´–ô –≥–µ–Ω–µ—Ä–∞—Ç–æ—Ä –º–µ–Ω—é
debug_log "–ó–∞–≥—Ä—É–∑–∫–∞ menu_generator.sh..."
source "${SCRIPT_DIR}/modules/core/menu_generator.sh"
source_exit_code=$?
debug_log "–ó–∞–≥—Ä—É–∑–∫–∞ menu_generator.sh –∑–∞–≤–µ—Ä—à–µ–Ω–∞ —Å –∫–æ–¥–æ–º: $source_exit_code"
if [[ $source_exit_code -ne 0 ]]; then
    echo -e "\n\n[FATAL] –ö–†–ò–¢–ò–ß–ï–°–ö–ê–Ø –û–®–ò–ë–ö–ê: –ù–µ —É–¥–∞–ª–æ—Å—å –∑–∞–≥—Ä—É–∑–∏—Ç—å —è–¥—Ä–æ –º–µ–Ω—é (menu_generator.sh). –°–∫—Ä–∏–ø—Ç –Ω–µ –º–æ–∂–µ—Ç –ø—Ä–æ–¥–æ–ª–∂–∞—Ç—å—Å—è."
    exit 1
fi
if ! command -v render_menu_items &>/dev/null; then
    echo -e "\n\n[FATAL] –ö–†–ò–¢–ò–ß–ï–°–ö–ê–Ø –û–®–ò–ë–ö–ê: –§—É–Ω–∫—Ü–∏—è render_menu_items –Ω–µ –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∞ –¥–∞–∂–µ –ø–æ—Å–ª–µ –∑–∞–≥—Ä—É–∑–∫–∏. –ü—Ä–æ–±–ª–µ–º–∞ –≤ —Ñ–∞–π–ª–µ menu_generator.sh."
    exit 1
fi

# ============================================================ #
#                     –î–ï–ô–°–¢–í–ò–Ø –ì–õ–ê–í–ù–û–ì–û –ú–ï–ù–Æ                   #
# ============================================================ #

# –≠—Ç–æ —Ñ—É–Ω–∫—Ü–∏–∏-–æ–±–µ—Ä—Ç–∫–∏ –¥–ª—è –¥–µ–π—Å—Ç–≤–∏–π, –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–Ω—ã—Ö –≤ –º–∞–Ω–∏—Ñ–µ—Å—Ç–µ
# —ç—Ç–æ–≥–æ —Ñ–∞–π–ª–∞. –û–Ω–∏ –ø–æ–∑–≤–æ–ª—è—é—Ç –≤—ã–∑—ã–≤–∞—Ç—å –ª–æ–≥–∏–∫—É –∏–∑ –¥—Ä—É–≥–∏—Ö –º–æ–¥—É–ª–µ–π.
_reshala_uninstall_wrapper() {
    run_module core/self_update uninstall_script
}

# ============================================================ #
#                     –ì–õ–ê–í–ù–ê–Ø –õ–û–ì–ò–ö–ê                           #
# ============================================================ #

# –£–Ω–∏–≤–µ—Ä—Å–∞–ª—å–Ω—ã–π –∑–∞–≥—Ä—É–∑—á–∏–∫ –∏ –∑–∞–ø—É—Å–∫–∞—Ç–æ—Ä –º–æ–¥—É–ª–µ–π
run_module() {
    local module_name="$1"
    shift
    local module_path="${SCRIPT_DIR}/modules/${module_name}.sh"

    if [ -f "$module_path" ]; then
        source "$module_path"
        # –ï—Å–ª–∏ —Ñ—É–Ω–∫—Ü–∏—è —Å—É—â–µ—Å—Ç–≤—É–µ—Ç –≤ –∑–∞–≥—Ä—É–∂–µ–Ω–Ω–æ–º —Ñ–∞–π–ª–µ, –≤—ã–∑—ã–≤–∞–µ–º –µ–µ
        if command -v "$1" &>/dev/null; then
            "$@"
        else
            log "–û—à–∏–±–∫–∞ –≤—ã–∑–æ–≤–∞: —Ñ—É–Ω–∫—Ü–∏—è '$1' –Ω–µ –Ω–∞–π–¥–µ–Ω–∞ –≤ –º–æ–¥—É–ª–µ '${module_name}'."
            printf_error "–û—à–∏–±–∫–∞: —Ñ—É–Ω–∫—Ü–∏—è '$1' –Ω–µ –Ω–∞–π–¥–µ–Ω–∞ –≤ –º–æ–¥—É–ª–µ '${module_name}'."
        fi
    else
        log "–ö—Ä–∏—Ç–∏—á–µ—Å–∫–∞—è –æ—à–∏–±–∫–∞: –º–æ–¥—É–ª—å '${module_name}' –Ω–µ –Ω–∞–π–¥–µ–Ω."
        printf_error "–ú–æ–¥—É–ª—å '${module_name}' –æ—Ç—Å—É—Ç—Å—Ç–≤—É–µ—Ç. –£—Å—Ç–∞–Ω–æ–≤–∫–∞ –ø–æ–≤—Ä–µ–∂–¥–µ–Ω–∞."
    fi
}

# –ì–ª–∞–≤–Ω–æ–µ –º–µ–Ω—é
show_main_menu() {
    # –ü–µ—Ä–µ—Ö–≤–∞—Ç—ã–≤–∞–µ–º Ctrl+C —Ç–æ–ª—å–∫–æ –≤ –≥–ª–∞–≤–Ω–æ–º –º–µ–Ω—é, —á—Ç–æ–±—ã –≤—ã–≤–µ—Å—Ç–∏ —Å–æ–æ–±—â–µ–Ω–∏–µ
    trap 'printf "\n%b\n" "–î–ª—è –≤—ã—Ö–æ–¥–∞ –∏–∑ –†–µ—à–∞–ª—ã –∏—Å–ø–æ–ª—å–∑—É–π [q]." >&2' SIGINT

    while true; do
        run_module ui/dashboard show
        
        if [[ ${UPDATE_AVAILABLE:-0} -eq 1 ]]; then
            printf_critical_warning "–î–û–°–¢–£–ü–ù–û –û–ë–ù–û–í–õ–ï–ù–ò–ï! ${VERSION} -> ${LATEST_VERSION}"
        fi
        
        printf "\n%s\n\n" "–ß—ë –¥–µ–ª–∞—Ç—å –±—É–¥–µ–º, –±–æ—Å—Å?"
        
        # 1. –†–µ–Ω–¥–µ—Ä–∏–º –≤—Å–µ –ø—É–Ω–∫—Ç—ã –º–µ–Ω—é –∏–∑ 'main'
        render_menu_items "main"

        echo ""

        # 3. –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º —Å–ø–µ—Ü–∏–∞–ª—å–Ω—ã–µ, –∫–æ–Ω—Ç–µ–∫—Å—Ç–Ω–æ-–∑–∞–≤–∏—Å–∏–º—ã–µ –ø—É–Ω–∫—Ç—ã
        if [[ ${UPDATE_AVAILABLE:-0} -eq 1 ]]; then
            printf_menu_option "u" "‚ÄºÔ∏è –û–ë–ù–û–í–ò–¢–¨ –†–ï–®–ê–õ–£ ‚ÄºÔ∏è" "${C_BOLD}${C_YELLOW}"
        fi
        
        if [ "${SKYNET_MODE:-0}" -eq 1 ]; then
            printf_menu_option "q" "üîô –í–ï–†–ù–£–¢–¨–°–Ø –í –¶–£–ü" "${C_CYAN}"
        else
            printf_menu_option "q" "üö™ –í—ã–π—Ç–∏ –∏–∑ —Ä–µ—à–∞–ª—ã" "${C_CYAN}"
        fi
        print_separator "-" 60

        local choice
        choice=$(safe_read "–¢–≤–æ–π –≤—ã–±–æ—Ä, –±–æ—Å—Å") || {
            _LAST_CTRLC_SIGNALED=0;
            printf "\r\033[K%b" "${C_RED}üõë –ñ–º–∏ [q], —á—Ç–æ–±—ã –≤—ã–π—Ç–∏ –∏–∑ –≥–ª–∞–≤–Ω–æ–≥–æ –º–µ–Ω—é!${C_RESET}\n";
            continue;
        }
        
        # --- –û–ë–†–ê–ë–û–¢–ß–ò–ö –ù–ê–ñ–ê–¢–ò–ô ---
        local action
        # –°–Ω–∞—á–∞–ª–∞ –∏—â–µ–º –¥–µ–π—Å—Ç–≤–∏–µ –≤ –æ—Å–Ω–æ–≤–Ω—ã—Ö –ø—É–Ω–∫—Ç–∞—Ö
        action=$(get_menu_action "main" "$choice")
        
        if [[ -n "$action" ]]; then
            # –°–ø–µ—Ü-–æ–±—Ä–∞–±–æ—Ç–∫–∞ –¥–ª—è Skynet (–∑–∞–ø—Ä–µ—Ç –≤–ª–æ–∂–µ–Ω–Ω–æ—Å—Ç–∏)
            if [[ "$choice" == "0" && "${SKYNET_MODE:-0}" -eq 1 ]]; then
                printf_error "–¢—ã —É–∂–µ –≤ –º–∞—Ç—Ä–∏—Ü–µ."
            else
                # –í—ã–ø–æ–ª–Ω—è–µ–º –∫–æ–º–∞–Ω–¥—É, —Å–æ–±—Ä–∞–Ω–Ω—É—é –≥–µ–Ω–µ—Ä–∞—Ç–æ—Ä–æ–º
                eval "$action"
            fi
        else
            # –ï—Å–ª–∏ –¥–µ–π—Å—Ç–≤–∏–µ –Ω–µ –Ω–∞–π–¥–µ–Ω–æ –≤ –º–∞–Ω–∏—Ñ–µ—Å—Ç–∞—Ö, –ø—Ä–æ–≤–µ—Ä—è–µ–º —Å–ø–µ—Ü–∏–∞–ª—å–Ω—ã–µ —Å–ª—É—á–∞–∏
            case "$choice" in
                u|U)
                    if [[ ${UPDATE_AVAILABLE:-0} -eq 1 ]]; then
                        run_module core/self_update run_update
                    else
                        # –ï—Å–ª–∏ –ø—É–Ω–∫—Ç –Ω–µ –æ—Ç–æ–±—Ä–∞–∂–∞–µ—Ç—Å—è, —ç—Ç–æ –Ω–µ–≤–µ—Ä–Ω—ã–π –≤–≤–æ–¥
                        printf_error "–ù–µ—Ç —Ç–∞–∫–æ–≥–æ –ø—É–Ω–∫—Ç–∞."
                    fi
                    ;;
                q|Q)
                    if [ "${SKYNET_MODE:-0}" -eq 1 ]; then
                        exit 0
                    else
                        echo "–ë—ã–ª —Ä–∞–¥ –ø–æ–º–æ—á—å. –ù–µ –æ–±–æ—Å—Ä–∏—Å—å. ü•É"
                        exit 0
                    fi
                    ;;
                *)
                    printf_error "–ù–µ—Ç —Ç–∞–∫–æ–≥–æ –ø—É–Ω–∫—Ç–∞."
                    ;;
            esac
        fi
    done
}

# ============================================================ #
#                       –¢–û–ß–ö–ê –í–•–û–î–ê                            #
# ============================================================ #
main() {
    init_logger

    if [[ $EUID -ne 0 ]]; then
        printf_error "–≠—Ç–æ—Ç —Å–∫—Ä–∏–ø—Ç –¥–æ–ª–∂–µ–Ω –±—ã—Ç—å –∑–∞–ø—É—â–µ–Ω –æ—Ç –∏–º–µ–Ω–∏ root –∏–ª–∏ —á–µ—Ä–µ–∑ sudo."
        exit 1
    fi

    if [[ "${1:-}" == "install" ]]; then
        source "${SCRIPT_DIR}/modules/core/self_update.sh" && install_script
        ensure_package "sudo"
        exit 0
    fi

    log "–ó–∞–ø—É—Å–∫ —Ñ—Ä–µ–π–º–≤–æ—Ä–∫–∞ –†–µ—à–∞–ª–∞ ${VERSION}"
    run_module core/self_update check_for_updates
    show_main_menu
}

main "$@"
